     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
        $g = driverReequests::find($id);

        switch ($g->status) {
            case 'accepted':
                driverReequests::where('id', $id)
                    ->update(['status'=>'started']);
                break;
            
            case 'started':
                    driverReequests::where('id', $id)
                        ->update(['status'=>'stopped']);
                    break;
                
            default:
                return back();
                break;
        }

        
        
        return back();
    }

